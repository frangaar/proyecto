class ColisionablesObjects {
    x
    y
    width
    height
    constructor(x, y, width, height) {
        this.x = x
        this.y = y
        this.width = width
        this.height = height
      }
      get getX() {
        return this.x
      }
      get getY() {
        return this.y
      }
      get getWidth() {
        return this.width
      }
      get getHeight() {
        return this.height
      }
     
}